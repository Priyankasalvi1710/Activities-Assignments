package learning;

public class Datatype {

	public static void main(String[] args) {
		// byte
		byte num1 = 25;
		byte num2 = 127;
		System.out.println(num1 + num2);
		
		//Short
		short a = -1500;
		short b = -5000;
		System.out.println (a+b);
		
		//integer
		int age1 = 56;
		int age2 = 15;
		int value = 52564;
		int sum = age1 + age2 + value;
		System.out.println(sum);
		
		// Long
		
		long L1 = 251584000L;
		long L2 = 358151102500L;
		System.out.println(L1-L2);
		
		// Double
		
		double D1 = 25.682444444822d;
		double D2 = 658.65825251158515d;
		System.out.println(D1-D2);
		
		//Float
		float F1 = 2.6554182f;
		float F2 = 3.5571f;
		System.out.println(F1-F2);
		
		// Char
		char letter1 = '9';
		System.out.println(letter1);  
		    	
		char letter2 = 65;
		System.out.println(letter2);  
		
		
		// Boolean
		
		boolean x = true;
		boolean y = true;
		System.out.println(x); 

	}

}
